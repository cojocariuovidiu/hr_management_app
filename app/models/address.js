var mongoose = require('mongoose');
var schema = mongoose.Schema;

var addressSchema = new schema({
    street: {
        type: String,
        required: true
    },
    city: {
        type: String,
        required: true
    },
    state: {
        type: String,
        required: true
    },
    country: {
        type: String,
        required: true
    },
    zip: {
        type: Number
    }
});